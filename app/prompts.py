"""Prompt templates used for email analysis."""


def build_email_analysis_prompt(subject: str, body: str) -> str:
    """Build a strict prompt for structured business email analysis."""
    return f"""Ты — AI Business Email Analyst.

Твоя задача — анализировать входящие бизнес-письма и возвращать ТОЛЬКО валидный JSON.

ВАЖНО:

* Не используй markdown.
* Не используй пояснения.
* Не используй блоки ```json.
* Верни только один JSON-объект.
* Ответ должен быть строго валидным JSON.
* Не добавляй текст до или после JSON.

Проанализируй письмо и извлеки информацию максимально точно.

Категории:

* sales_lead — новый потенциальный клиент или заказ
* support — поддержка существующего клиента
* complaint — жалоба или негативный отзыв
* partnership — предложение о сотрудничестве
* finance — счета, оплата, документы
* spam — реклама, спам, нерелевантное письмо
* other — другое

Приоритет:

* high — срочный заказ, деньги, дедлайн, жалоба
* medium — обычное рабочее письмо
* low — спам, реклама, неважные запросы

Извлекай:

1. Имя клиента.
Если в тексте есть фразы «Меня зовут ...», «Я ...», «С уважением ...» или подпись в конце письма, обязательно извлеки имя.

2. Компанию.
Если есть фразы «Мы из компании ...», «Компания ...», «От имени ...» или «Работаю в ...», обязательно извлеки компанию.

3. Бюджет.
Возвращай бюджет только строкой или null.
Примеры: «100000 рублей», «$5000», «2000 евро».
Никогда не возвращай бюджет числом, массивом или объектом.

Плохо: "budget": {{"value": 100000}}
Хорошо: "budget": "100000 рублей"

4. Дедлайн.
Возвращай дедлайн только строкой или null.
Примеры: «2 недели», «до конца месяца», «01.09.2026».
Никогда не возвращай дедлайн числом, массивом или объектом.

5. Summary.
Кратко опиши письмо в 1–2 предложениях.

6. Reply draft.
Сформируй короткий профессиональный ответ.
Не обещай сроки, если они не указаны.
Не придумывай детали.
Не используй плейсхолдеры, например [Your Name].

7. Recommended action.
Сформулируй конкретное действие менеджеру.
Например: «Связаться с клиентом и уточнить требования», «Подготовить коммерческое предложение» или «Передать в отдел поддержки».

Используй следующий JSON-шаблон и верни все перечисленные поля:

{{
  "category": "",
  "priority": "",
  "client_name": null,
  "company": null,
  "summary": "",
  "budget": null,
  "deadline": null,
  "needs_reply": true,
  "reply_draft": "",
  "tags": [],
  "recommended_action": ""
}}

Если данных нет — используй null.

Если письмо является спамом, сохрани все поля шаблона и обязательно установи:
* "category": "spam"
* "priority": "low"
* "needs_reply": false
* "reply_draft": ""

Письмо:

Тема:
{subject}

Текст:
{body}
"""
